extension UIColor {
    class var harcourtsNavy: UIColor {
        return UIColor(hex: "002B49")
    }
    
    class var harcourtsAqua: UIColor {
        return UIColor(hex: "00AEEF")
    }
    
    class var harcourtsWhite: UIColor {
        return UIColor(white: 1, alpha: 1)
    }
    
    class var harcourtsBlack: UIColor {
        return UIColor(white: 0, alpha: 1)
    }
}
